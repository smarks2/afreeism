<p>
Abby is retired and gets a tremendous benefit from not having to get up and go to work every day.  Leisure is worth a lot to her and she consumes a lot of it.  Bob, on the other hand, is working two jobs and gets very little time for leisure.  Perhaps he is early in his career and wishes to get ahead quickly.  The other members of this 5-person society are somewhere in-between.  If we look at just the value of goods and services, we would conclude that the society is very unequal.  However, looking at total value, including leisure, we see that this is a very egalitarian society after all.
</p>
<p>
In addition, people have different ways of distributing the consumption of goods, services, and leisure over their lifetimes.  Some may prefer more consumption now and less later.  Others may have the opposite preference.
</p>
<p>
Complicating matters is that it is not easy to measure value.  Some goods are priced by the market, so at least at the margins we have an estimate of value.  Other goods, such as clean air or a crime-free neighborhood, are more difficult to price.  Even with market goods, extreme maldistribution of buying power means that the market prices of many goods do not represent their value, especially to those who cannot afford them.
</p>
<p>
All of this is to say that the measurement of societal benefits is not easy.  Nevertheless we may be able to draw some rough conclusions.  
</p>
<h2>
Inequality in the United States
</h2>
<p>
In spite of the difficulty in measuring societal benefits, it is not hard to determine that benefits in the United States and in the world are grossly unequal.  Poverty in the United States is a big societal problem.  This problem of poverty is connected to massive inequality in income and wealth.  So much income and wealth is concentrated in the top one percent (and one-tenth of one percent) of the population that there is insufficient wealth or income remaining to alleviate poverty.  The tax system exacerbates poverty.  (As Warren Buffet says, his secretary pays a greater percentage of her income in taxes than he does. )  Wealth and income also have a racial dimension.  African-Americans and Latinos have far lower income and wealth than whites.  Here are some facts:
</p>
<p style="margin-left:20px;">
•	In 2017, over 12% of the population (about 40 million people) live in poverty. 
</p>
<p style="margin-left:20px;">
•	About one-third of the population (approximately 100 million people) live in poverty or near-poverty. 
</p>
<p style="margin-left:20px;">
•	As of 2019, the three wealthiest individuals in this country (Bezos, Gates, and Buffet) have more wealth than the bottom 50% of the population (over 160 million people). 
</p>
<p style="margin-left:20px;">
•	The top 400 richest people in this country (.00025 percent of the population) now have more wealth than the bottom 63% of the population (over 200 million people) 
</p>
<p style="margin-left:20px;">
•	Among families in the United States, the top 1% have almost twice as much wealth than the bottom 90% of families.  Indeed, the top 0.1% of families have approximately the same amount of wealth as the bottom 90% of families. 
</p>
<p style="margin-left:20px;">
•	Medium family wealth, when measured by race, is highly disparate:   White¬—$171,000; Black—$17,150;  Latinx—$20,720.   (These are conservative estimates.  Other estimates show even greater disparities. )
</p>
<p>
Afreeism says that we cannot justify these disparities by deservedness.  So the question is whether there exists any other justification for such disparities.
</p>

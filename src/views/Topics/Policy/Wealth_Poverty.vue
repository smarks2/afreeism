The question is, can such inequality of income and wealth be justified by incentives.  A couple of thought experiments should make the answer to this question fairly obvious.  
</p>
<p style="margin-left:20px;">
1.	Suppose that you had told the young Jeff Bezos, before he ever started working on what would become Amazon, that he stood to gain, say, $10 billion in personal wealth if Amazon were successful instead of the more than $100 billion that he is currently worth, would he have made the same efforts?  (Clearly the answer is yes.)  
</p>
<p style="margin-left:20px;">
2.	Suppose you had told hedge fund manager James Simons in 2018 that his income would be 90% lower, a mere $160 million for the year.  Would he have refused to work?  (No.)  If you had told him before he ever became a hedge fund manager, that his top salary would be $160 million a year or lower, would that have deterred him?  (No.)
</p>
<p>
Of course, you do not have to be an afreeist to be troubled by the extremes of wealth and poverty.  Nor do you have to be an afreeist to come to the conclusion that these extremes cannot be justified by incentives.  What afreeism brings to the table is that these extremes cannot be justified by deservedness either.  None of these people deserve what they got.  Nor do people deserve to live in poverty.
</p>
<p>
In a way, this is another facet of what we discussed in Chapter 8 on criminal law.  The determinism of the universe means that we should not inflict retribution on people.  Likewise, people do not deserve the abject poverty that many live in.  Nor do the wealthy deserve their massive share of the benefits of the earth and society.  However, just as in the criminal law we tolerate some punishment for the sake of deterring bad behavior and protecting society, we may want to tolerate some inequality for the sake of creating incentives.   In the case of criminal law, we should seek to minimize punishment to a level that still provides adequate safety for the population.  Likewise, with broader social policy we should seek to minimize inequality to a level that provides adequate incentives.
</p>
<h2>
Fixing Inequality
</h2>
<p>
Detailed policy recommendations on how to fix the massive inequality in the United States and in the world is beyond the scope of this book.  Clearly, wealth and income need to be redistributed significantly.  It is also clear that, given the huge disparities of wealth and income, this can be done without significant effects on incentives.  How it is done requires a lot of thought.  
</p>
<p>
For a start, we could tax the incomes of Jeff Bezos and hedge fund manager James Simon at significantly higher rates.  The current system in the United States is perverse in this regard. Someone who makes $60,000 a year pays a greater share of their income in taxes than someone who make more than $1 million when all taxes (income, payroll, sales, etc.) are taken into account.  Again, as Warren Buffet has often pointed out, he pays a lower percentage of his income in taxes than does his secretary.   Wealth taxes should also be on the table as should minimum wage laws.  Universal health care (depending on how it is paid for) could also mitigate wealth disparities, as would a more robust social security system.
</p>
<p>
Money raised using progressive income taxes or wealth taxes could be used to reduce poverty through subsidies such as the Earned Income Tax Credit or through the provision of services, such as health services and investments in public education.  
</p>
<p>
These are just some ideas.  Afreeism does not favor one policy over another.  There is much hard work to be done to figure which policies will help transform the economy to one that more fairly distributes costs and benefits.
</p>
<h2>
The Bottom Line
</h2>
<p>
As we look forward to the future through the lens of causation, we can see that what we do makes a difference.  Although afreeism is not a political philosophy, it does teach us to look at the world in a different way.  Once we discard the blinders of deservedness, we see with clarity how unjustified it is for some to have so much wealth while others live in abject poverty.  No one deserves this.  Our actions could help change this. 
</p>


    <div class="TopicHeading">Wealth and Poverty</div>
      <p>
In a modern economy, people work, almost always collectively, to produce goods and services.  For the most part, these goods and services are consumed by people other than the producers.  In the process, wealth is created.  The economic system affects what goods and services are produced, how they are produced and by whom, and how they are distributed to members of society.  Economic policy is therefore key to the achievement of a good society, a society that we would like to live in.
</p>
<p>
We therefore must make choices about how our economy is organized.  Some of us do so directly.  That is, we may work for the government and be involved in policy decisions.  At a minimum, most of us can vote.  
</p>
<p>
There is no such thing as a completely laissez-faire position when it comes to economic policy.  If one believes in markets, for example, then the institutional underpinnings of a market economy must be put in place.  Such an economy needs the concept of private property and the means to protect property rights.  Thus it needs property rules and the courts and police to protect them.  And even the most ardent marketeers concede that there are some products and services, such as national defense, that the government should provide.  Others believe that the government should have a role in infrastructure creation and maintenance, such as in the area of transportation (roads and bridges), the power grid, and the internet.
</p>
<p>
Other members of society would encourage additional governmental intervention.  They support regulation, such as the highly successful Clean Air Act, the provision of medical insurance under Medicaid and Medicare, and retirement funding under Social Security.  Among those who agree on these things, there are a variety of viewpoints regarding the level of funding and the breadth of benefits.  Others support minimum wage laws, the regulation of medicines, food labeling.
</p>
<p>
My only point, so far, is that we as a society have decisions to make.  How we distribute the benefits of the economic system is one of those decisions. Although afreeism has little to say about specific policies, it does provide a valuable principle as a starting point.  This principle, which I discussed in Chapter 6, is the Equality Principle.  Here it is again:
</p>
<p style="margin-left:20px;">
1.	 Unless there are reasons for doing otherwise, the costs and benefits of society should be distributed equally.  
</p>
<p style="margin-left:20px;">
2.	Deservedness cannot be a reason for distributing the costs and benefits of society unequally.
</p>
<p>
The equality principle comes from the afreeist conclusion that there is no such thing as deservedness.  Because the world is deterministic, successes and failures are just the product of a causal web. Outcomes could not have been otherwise.  Thus, no one deserves anything.
</p>
<p>
Deservedness cannot be a reason to deviate from equality.  However, there may be reasons other than deservedness not to distribute wealth and income equally.  We should scrutinize and analyze these reasons carefully.   I will argue that even with these reasons, deviations from equality should be modest.  Unfortunately, they are not.
</p>
<h2>
Measuring Societal Benefits Correctly
</h2>
<p>
Having equality of benefits does not mean everyone will end up with identical baskets of goods and services.  People have different values and will choose (deterministically) their desired basket among those available.  For example, some people value leisure more than others, or value leisure more during some times in their lives than in other times.  If we looked at just goods and services, we might observe inequality, but if we factor in that some people are working harder than others then we might see less inequality.  
</p>
<p>
Some people may choose more leisure over more income.  The value of leisure that they are getting compensates for the lower income.  Suppose, for example, that we can put a dollar value on leisure.  Now consider the following distribution: 

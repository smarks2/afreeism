<h2>
Incentives
</h2>
<p>
We have already determined that the distribution of resources should not be based on who deserves it.  In the deterministic universe, no one deserves anything.  However, we also determined that we are willing to sacrifice some equality for measures that increase the size of the pie significantly.  Incentives are one way to increase the size of the pie.  That is, we give rewards to those who are willing to educate themselves, to work, and to take reasonable risks.
</p>
<p>
So, the question becomes, how unequal must the distribution of societal benefits be in order to provide reasonable incentives for efficiency?  This is not easy to quantify precisely.  It depends on knowing how much additional output greater incentives will produce, how much societal value this additional output will produce, how much inequality will be sacrificed, and how much we value that loss of equality.  The question is difficult, but let us give it a try.
</p>
<p>

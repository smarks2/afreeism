Bill then asks the assembled business leaders to write down on a piece of paper the amount that they would, in this situation, bid.  He then asks, for a show of hands.  How many wrote down 25% or less.  Zero.  How many wrote down 50%?  Zero.  How many wrote down 75%?  Three.  How many wrote down 100%?  Everyone else.
</p>
<p>
Chuck Collins uses this story to combat what he calls the myth of deservedness, the idea that a person somehow deserves everything they get because they are smart and worked hard.  This is a myth that affects many people, but particularly the very rich.  Bill Gates Sr.’s parable helps people realize that accomplishment is the result of many, many causal factors.  Intelligence and hard work are just a small part of this.
</p>
<p>
<b>The Illusion of Accomplishment</b>
</p>
<p>
Chuck and Bill’s story is a step in the right direction.  However, it does not go nearly far enough.  First, because the universe is deterministic, all actions and outcomes are determined.  Thus, even if one’s accomplishments were solely the result of intelligence and hard work, the benefits of accomplishment are still not deserved.  Both intelligence and the propensity to work hard are completely determined by causes existing before the actor was born.  This is not a question of nature versus nurture.  Both nature and nurture are determined.  Thus, the actions taken and the success achieved are all completely determined.  They simply had to be.  It thus makes no sense to say that someone deserves a large income, vast wealth, or even happiness.  If we do praise or reward someone for supposed accomplishments, it has to be based on some other reason and not on the myth that someone deserves it for what they did.
</p>
<p>
<b>The Illusion of Failure</b>
</p>
<p>
The other side of the coin is that failure is undeserved as well.  One aspect of this is moral failure, which we discussed in Chapter 3 on moral responsibility.  Moral failure can cause pain, suffering, and loss.  Nevertheless, we concluded that no one deserves blame or punishment based on moral failure because she could not have done otherwise.  Put another way, we are all innocents.  All of us.  All the time.
</p>
<p>
As a result, we cannot justify punishment on the basis that a person deserves to be punished.  Retribution, the notion that people should get what they deserve, never makes sense because people never deserve anything.  This is not to say that we should never punish.  However, punishment must be based on other principles besides deservedness, for example, on principles of deterrence, public health, education, incapacitation or the like.  These are not easy arguments to make, since they all must deal with the notion that no matter what we do, punishment always involves punishing innocents.  (After all, everyone is always innocent.)  In a deterrence model, for example, we are punishing innocents in order to deter others.  How can this be morally justified?  Chapter 8 examines these issues of crime and punishment.
</p>
<p>
Other types of failure are also completely determined.  If one fails at business, one does not deserve the consequences of that failure.  Likewise, one does not deserve the consequences of failing health, even if one contributed to it by overeating, failing to exercise, overusing alcohol or recreational drugs, or engaging in risky activities.  All of these actions were determined, as were the consequences.  There is no sense in which any of the consequences are deserved.  Again, that does not mean that we never permit or impose negative consequences.  There may be good reasons to do so.  Deservedness, however, is not one of them.
</p>



In Chuck Collin’s book, Born on Third Base, Chuck describes a talk that Bill Gates Sr. gave to a group of business leaders.  (Chuck and Bill were promoting a book arguing in favor of the inheritance tax.)  Bill tells a story.  Suppose God is about to send two new souls to earth, two new potential entrepreneurs.  The following are Bill’s words: 

  <div class="TopicHeading">The Equality Principle</div>
<p>
The illusion of accomplishment and the illusion of failure are both manifestations of the myth of deservedness.  Understanding this has profound consequences for how we view social policy.  Because the universe is deterministic, who is rich and who is poor is not a matter of who deserves to be rich and who deserves to be poor.  It is hard to justify in any moral sense why some people die of starvation or live in wretched conditions while others have wealth that would be enough to feed, clothe, and take care of millions of people.
</p>
<p>
In short, there is no reason why equality of societal costs and benefits should not be a fundamental principle of our social policy.  Since no one deserves anything, we should try to distribute the benefits of society as equally as possible.  Imagine that you are given a million dollars and you are asked to distribute this money among 1000 people about whom you know nothing.  How would you do it?  Knowing nothing, you would likely distribute it evenly.  That will be our starting point.  We will deviate from this principle, of course, but our deviations will have to be carefully justified.
</p>
<p>
The Equality Principle is the following moral imperative:
</p>
<p>
1.	 Unless there are reasons for doing otherwise, the costs and benefits of society should be distributed equally.
</p>
<p>
2.	Deservedness cannot be a reason for distributing the costs and benefits of society unequally.
</p>
<p>
Note that the equality principle does not mean that resources and benefits have to be 
distributed equally.  It does say that to deviate from equality requires a justification and
that the justification cannot be deservedness.  
</p>


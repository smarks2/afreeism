<p>
Now consider two subjects.  Subject 1 is given $10 and told to buy whatever she wants.  Subject 2 is given $30 and told that the government has prohibited the sale of Good B, but other than that she can purchase whatever she wants.  Which subject is most free?  A believer that only negative freedom is freedom would say Subject 1, because there are no governmental restrictions.  Those who argue for freedom as defined by positive freedom would say Subject 2 is most free, because she has access to Goods A and C, whereas Subject 1 has access only to Good A.
</p>
<h2>
Types of Constraints
</h2>
<p>
Now what does determinism have to say about this?  First, all decisions are completely determined.  Thus, overall, we might conclude that we have no freedom.  And, in a sense, we would be right.
</p>
<p>
However, freedom is more complicated.  The above discussion on positive and negative freedom is instructive.  When we talk about freedom, we are not talking about absolutes.  When someone says that she is free, she is not saying that she is free to flap her arms and fly away.  Freedom refers to a lack of particular types of constraints.  For example, when we say that we have political freedom, we are saying that the government is not constraining us unduly.  When we say that we have economic freedom, we are saying that a lack of resources is not constraining us.  Thus we may possess political freedom or economic freedom in the deterministic universe.
</p>
<p>
To see how freedom differs from free will, imagine that we create a robot and give it a certain latitude for movement.  We could confine it to a 4 foot by 4 foot room.  This would limit its freedom of movement.  Or we could limit it to a 12 foot by 12 foot room.  Or we could let it go outside.  In each case there is a certain amount of freedom of movement.  We could have debates about how much freedom of movement the robot should have.  
</p>
<p>
Notice that we are talking about the freedom of a robot to move about even though we would never say that the robot has free will.  The robot’s actions are still deterministic.  Even if we give it unlimited freedom of movement, it may never go outside the original 4 by 4 foot area because other causal factors, including its programming, are constraining it.  
</p>
<p>
Freedom, then, only refers to freedom from a particular constraint.  Imagine a world in which an action is completely determined by a combination of the following constraints:
</p>
<p style="margin-left:20px;">
Constraint A (Political constraint)
</p>
<p style="margin-left:20px;">
Constraint B (Resource constraint)
</p>
<p style="margin-left:20px;">
Constraint C (Desire constraint)
</p>
<p style="margin-left:20px;">
Constraint D (Mobility constraint)
</p>
<p>
Assume that these constraints completely determine the action that the subject will take.  Some of these constraints may be binding, that is, they may actually restrict what the subject can do, and others may not be binding.  A constraint is binding if removing it would change the subject’s action.  If removing the constraint would not change the action then we say that the constraint was nonbinding.  
</p>
<p>
In our simple world, political freedom involves removing or reducing Constraint A.  This may or may not affect behavior.  Economic freedom would involve removing or reducing Constraint B.  Again, this may or may not affect behavior.
</p>
<p>
It should be clear that one can put the label of freedom on the removal or reduction of any constraint.  Stoics and Buddhists advocate removing Constraint C.  One is free, they say, if one removes the desire for things.  And clearly, giving wheelchairs to people in poor countries who cannot afford them (removing Constraint D), as one NGO does, increases tremendously the freedom of those receiving the wheelchairs.
</p>
<h2>
Afreeist Freedom
</h2>
<p>
If we design a robot and give it a task - for example, to maximize some goal - the amount of freedom of movement we give it may affect the extent to which it accomplishes its goal.  Suppose we have a robot that we give the task of collecting used containers for recycling.  Suppose we confine the robot to a one block area and a twenty-four hour time limit.  If we increase the robot’s freedom of movement, say, to ten blocks and its freedom of time to, say, one week, we may get better results.  The same principle applies to humans.  In addition, humans may actually value the freedom even if the results in terms of other societal benefits are the same.  The fact that we give the robot more freedom of movement, does not mean that the robot’s actions are not determined.  They are.  And so are the human’s actions.  Yet we may be happier and more productive with greater freedom.  
</p>
<p>
Afreeist freedom involves the removal of particular constraints on action.  When a binding constraint is removed, other constraints become binding.  For example, if I allow my young daughter to purchase concert tickets (removing the parental constraint), she then is faced with a monetary constraint that may limit her ability to buy the tickets or to get good seats.  There will always be binding constraints that completely determine a person’s actions.  (This is just another way of saying the universe is deterministic.)  Nevertheless, the removal of certain constraints may make us happier.  Removing constraints on exchange will allow us to exchange less valuable goods for more valuable goods.  Removing constraints on speech, may result in government action that benefits more of us.  It may also result in the exchange of ideas that further social and scientific progress.  Removing constraints on the practice of religion may result in activity that brings us greater happiness.  
</p>
<p>
We may decide to trade some freedoms for others, or even trade some freedoms off against other important social goals.  The freedom to assault our neighbor may be traded off for the right to be free from assault.  The freedom to pollute may be traded off against freedom from pollution.  The freedom to monopolize may be traded off against the freedom to compete in an open competitive market.  Afreeism recognizes that these freedoms exist and are important.  
</p>
<p>
One final thing.  You ask, is this not simply redefining freedom in the same way that compatibilists redefine free will?  Perhaps.  Still, this definition of freedom does not come with the same baggage as free will.  Free will, even compatibilist free will implies moral responsibility and the justification of retribution.   This in turn is used to justify the worst excesses of the criminal justice system and to justify extremes in wealth and poverty.  Afreeist freedom is limited, refers to very specific constraints, and does not imply that actions are in any way the product of free will.  Freedom is the removal of constraints on action.  Nothing more . . . but also nothing less. 
</p>


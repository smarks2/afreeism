      <p>
Wars have been fought in the name of freedom.  Songs have been written proclaiming it.  “My country ‘tis of thee, sweet land of liberty . . . let freedom ring.”  Our national anthem affirms that America is “the land of the free, home of the brave.”  New Hampshire license plates scream, “Live free or die!”  Dylan asked, “Yes, 'n' how many years can some people exist, before they're allowed to be free?”  
</p>
<p>
The market economy rests on the so-called freedom of exchange.  As the theory goes, individuals know better than anyone else what they value.  If I value apples more than oranges and you value oranges more than apples, and if we can freely trade, then I can trade one of my oranges for one of your apples.  Having the freedom to make that trade makes us both better off. 
</p>
<p>
Freedom also makes government and other institutions function better.  Freedom of speech, freedom of the press, and the freedom to assemble allows us to communicate with our government and with other institutions, to express dissatisfaction with their policies.  Freedom of religion and freedom from religion allow us the satisfaction of following our beliefs.
</p>
<p>
There is an ongoing philosophical debate on whether freedom is important in itself, and not just because it allows individuals to maximize their personal benefits by choosing among alternatives.  In other words, perhaps it just feels good to be free.    
</p>
<p>
But are we really free at all?  Does freedom really exist in a deterministic universe?  
</p>
<h2>Types of Freedom</h2>
<p>
We cannot change the fact that the universe is deterministic.  If this implies that we do not have freedom, so be it.  However, freedom is relative and what we mean by freedom is relative.  Although we are constrained by causal factors, these factors have different characteristics.  Much of the discussion around freedom has to do with types of constraints, not on whether people are constrained.  
</p>
<p>
For example, there is a large debate framed around positive freedom and negative freedom.  Negative freedom is defined as “freedom from” and positive freedom as “freedom to”.  In particular, those touting negative freedom see it as freedom from restrictions on action imposed by other human beings, particularly by governments.   Any form of restriction imposed on an individual by a government reduces negative freedom.  Positive freedom, on the other hand, looks at a wider array of constraints on action.  For example, someone who has fewer of society’s resources has fewer options as to what and how much to consume.  They are less free in the positive sense.
</p>
<p>
These two freedoms are often in conflict.  Consider the following distributions:

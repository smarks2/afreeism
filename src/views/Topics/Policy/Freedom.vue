Suppose that Distribution A represents a fairly unregulated economy.  The economy produces goods (food, shelter, etc.) and bads (pollution, etc.)  Suppose that the government steps in and regulates air quality and provides financial incentives for research and development, such as a patent system that restricts the ability of competitors to use patented technology for a number of years.  As a result, suppose, the economy improves, as represented by Distribution B.  Not only do the members of our society get cleaner air, they also get access to a greater number of products.  In this case, negative freedom went down, due to increased government restrictions, but positive freedom went up.
</p>
<p>
Proponents of negative freedom do not recognize positive freedom as freedom.  Only negative freedom is freedom.  Isaiah Berlin states,
</p>
<p>
If I say that I am unable to jump more than ten feet in the air, or cannot read because I am blind, or cannot understand the darker pages of Hegel, it would be eccentric to say that I am to that degree enslaved or coerced.  Coercion implies the deliberate interference of other human beings within the area in which I could otherwise act. 
</p>
<p>
Thus, according to Berlin, merely not having enough resources to buy something is not a lack of liberty.  Berlin, and others, did realize that negative liberty was not absolute.  Liberty could indeed be traded off against other values, such as a fairer distribution of resources.  What he objected to was calling this an increase in freedom, even though many members of the society would have more alternatives available to them.
</p>
<p>
Of course, others disagree with Berlin.  Picture subjects being put in a room with a vendor of goods.  Suppose that these goods are priced as follows:

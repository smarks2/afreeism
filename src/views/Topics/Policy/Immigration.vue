<div class="TopicHeading">Immigration</div>
<p>
There is nearly unanimous agreement among policy makers that the immigration system needs major reform.  What does afreeism say about immigration?  First, as everyone recognizes (not just afreeists), one does not choose where one is born.  One does not deserve to be born in a developed country; nor does one deserve to be born in a less developed country.  
</p>
<p>
Yet, many people in developed countries believe that it is right to exclude others from the opportunities and benefits that come from being born in a developed country.  It is unclear where this feeling of entitlement originates.  
</p>
<p>
Afreeism teaches us that no one deserves anything ever.  The resultant Equality Principle counsels us to promote equality unless there is a compelling reason not to do so.  Deservedness is not a reason.  
</p>
<p>
One way to promote equality of opportunity and benefits is through a generous immigration policy.  This policy would allow as much immigration as possible consistent with maintaining an orderly and productive system.  The Equality Principle can be reframed in the immigration context:
</p>
<p>
1.	Unless there are reasons for doing otherwise, people should have the freedom to move wherever they want.
</p>
<p>
2.	Deservedness cannot be a reason for limiting freedom of movement.
</p>
<p>
Many immigrants are fleeing poverty and crime in the countries where they were born.  Many immigrants currently at the southern border of the United States, for example, have had family members murdered by gangs, and have been threatened with being killed themselves.  Others are simply seeking economic opportunity in a place where they can raise their families in peace and provide their children with good nutrition, good shelter, and good education.  
</p>
<p>
There is no good reason why we should not let in many more immigrants than we do now, families who are fleeing oppression, violence, and poverty and who are seeking a better life.  Naturally, we should do so in a controlled way, so as to provide the kind of functioning economy that benefits everyone.  We, in the United States and in many other countries of the developed world, have the resources to do so.  We should be as generous with those resources as possible.  There but for the grace of the universe go we.
</p>
<p>
Note that the immigration issue is related to two others.  First, the issue of immigration is related to the issue of inequality in the United States.  Because of the extreme inequality in the United States, there is a significant amount of poverty.  Even the middle classes have lifestyles that are significantly worse than they could be if income and wealth were more fairly distributed.  Because many people see immigration as further eroding their economic and social positions, they oppose it.  In contrast, those who are better off tend to support more generous immigration policies.   Thus, we can conclude that if we address the extreme inequality in income and wealth in the United States, and thereby raise incomes for the vast majority of residents, there should be greater popular support of a more generous immigration policy.
</p>
<p>
There is also an important relationship between immigration and development.  Most immigrants do not leave their countries of origin – countries where they grew up, where they have family, relatives, and friends, countries where they speak the language and value the culture – without good reason.  Most are fleeing poverty and violence.  Development aid can improve living conditions, increase economic opportunity, reduce crime, and increase safety in many of these countries.  That is, development aid can reduce the demand for immigration.  In the run-up to the 2020 United States presidential election, a number of candidates have recognized this interconnection.  One has even called for a Marshall Plan for Central America. 
</p>


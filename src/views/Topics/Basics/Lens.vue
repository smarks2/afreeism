    <div class="TopicHeading">The Lens of Causation</div>
<P>
As we saw from the previous chapters, our view of the world still makes a lot of sense even when we realize that we do not have free will.  We still operate as autonomous individuals.  We deliberate and make decisions, we weigh costs and benefits, we communicate with our fellow humans in the usual ways, and we take action.
</p>
<p>
Yet not everything remains the same.  Some things will change.  Realizing that the world is deterministic and that we have no free will makes us look at some things very differently.  In order to understand the afreeist perspective, we must keep in mind two things:
</p>
<p style="margin-left:20px;">
1.	The universe unfolds through a causal chain, and
</p>
<p style="margin-left:20px;">
2.	Causation looks quite different depending on whether we are looking to the future or looking at the past.
</p>
<p>
The lens of causation illustrates the dual nature of causality.  Imagine a landscape of time going forward into the future and backwards into the past.  You observe both the future and the past through a special lens, the lens of causation.  What do you see?  First, look forward into the future.  You cannot see the future with precision through this or any other lens.  Instead, roads spread out in front of you and go off into the distance.  These roads fork in a seemingly infinite pattern of possibilities leading to outcomes ranging from the sublime, to the beautiful, to the mundane, to the terrifying.  Sometimes you cannot see where a road is going, or you cannot see it clearly.  The road may disappear over a hill, for example.  But you can see many of the roads and forks.  Robert Frost poetically described such a fork in the road:
</p>
<p style="margin-left:20px;">
Two roads diverged in a yellow wood,
<br />
And sorry I could not travel both
<br />
And be one traveler, long I stood
<br />
And looked down one as far as I could
<br />
To where it bent in the undergrowth;
<br />
Then took the other, as just as fair,
<br />
And having perhaps the better claim,
<br />
Because it was grassy and wanted wear;
<br />
Though as for that the passing there
<br />
Had worn them really about the same . . . 
</p>
<p>
Some of the forks in the roads represent choices that you must make.  In decision theory, these are called choice nodes.  Other forks represent paths that could turn out one way or the other unpredictably, so-called random events.  These forks are, in decision theory, chance nodes. We assign probabilities to the various branches of a chance node.  (Recall from Chapter 2 that a probability represents a lack of knowledge and an estimate of likelihood.)
</p>
<p>
Looking forward into the future through the lens of causation, you can see that what you do, what you choose, makes a difference.  You may not be able to predict exactly what will happen, but you can have some influence over it.  Taking different roads leads to different outcomes.  Due to causation, the decisions that you make will, along with other factors, determine the outcomes.
</p>
<p>
In a sense, looking at the future through the lens of causation looks a lot like what all of us do naturally.  (It is also what our robots in Robot World do.)  We come to decision nodes and we try to predict the outcomes from making one decision or another.  Because we have the knowledge and understanding that events in the universe are caused, we can make predictions, although often imperfect ones.  If we know enough about the causes, we can make good predictions.  If we know less, then our predictions will be worse.  Nevertheless, it is clear that what we do matters.
</p>
<p>
Now let us suppose now that you have come to a fork, to a choice node, and you must decide whether to take one road or another.   You look ahead and, using causation, try to determine how your choice will affect your life and the lives of others.  You weigh your options and make a choice.  You proceed down one of the roads.
</p>
<p>
Now turn around and look through the same lens of causation towards the past.  It looks quite different than you remember.  The beautiful pattern of possibility has disappeared.  All you can see is one road as far back as you can see.  It is the road that you have traveled.  You see no other roads.  You see no forks, no crossroads.  Because of causation, there was in reality only one road.  There was only one action you could have taken.
</p>
<p>
What does this mean?  First, looking ahead you see alternatives.  (Recall how we defined alternatives in the previous chapter.)  Because the world is causal, what you do matters.  What road you take will determine (along with a host of other factors), where you end up.  From your perspective, then, it is important to plan well, to research your options, and to make the best choices you can with the information you have.  As part of your decision making algorithm, you likely will take into account what you value in life, your feelings and emotions, your relationships with people around you, and your philosophical view of what is the good society, among other things.  (Of course, as with our robots, your algorithm is also determined.  Some people may not consider any of these things.)  
</p>
<p>
Again, causation implies that what you do matters.  It affects how happy you will be and how happy those you care about will be and, in some small way perhaps, the type of world you and those who come after you will live in.  That is what causation says.  What you do causes a host of other events and actions stretching into the future.  You are a cause.  A caused cause.
</p>
<p>
However, once you have made a decision and have, so to speak, moved past that fork in the road, you will now be able to turn around look at that decision in the past through that same lens of causation.  All of the untaken options now have disappeared, and you realize that the action that you took was the only action possible.  This hindsight perspective is immensely freeing.  You cannot feel guilty, ashamed, remorseful or regretful, after all, for actions that simply had to be.  Nor can you blame others for actions that they have taken.  It was all determined.
</p>
<h2>
Determinism versus Fatalism
</h2>
<p>
Still, some people believe that if the world is determined, and outcomes set in stone, then it is pointless to try to improve ourselves or to improve the world.  What is going to happen, will happen.  This misunderstands determinism and afreeism.  It is precisely because the world is causal that what we do makes a difference.  Afreeism is a philosophy of action, not of futility.  
</p>
<p>
One reason that people assume that determinism implies futility is that they confuse fatalism and determinism.  Fatalism says that a specific outcome is inevitable no matter what a person does.  To say that I am fated to die at the age of 43, for example, is to say that, no matter what I do, no matter how well I eat, no matter how much exercise I get, no matter how many or few life-threatening activities I engage in, I am fated to die at 43.   Fatalism says that outcome C will occur irrespective of whether I take action A or action B.
</p>
<p>
This is not determinism.  Because the universe is deterministic, it may be true that I will die at the age of 43, but I will do so because of actions that I take, as well as the result of other factors unrelated to my actions.  If I had gotten more exercise, eaten better, gone to the doctor more often, then I would not have died at the age of 43.  Unfortunately (for me), I could not have done otherwise than what I did.  The causal chain led inevitably to me getting less exercise, eating unhealthily, avoiding my doctor, etc.  Thus, although I am one of the causes of my own demise, I could not have done otherwise.   
</p>
<p>
Consider this again in the context of Robot World.  If we program a robot to eat healthily, exercise, and get regular checkups, it will likely do better than a robot who we do not program to do these things.  And it makes perfect sense for one robot to communicate this to another robot.  If you exercise, eat healthily, get regular checkups, you will live longer. Such statements can be processed by the robot algorithm and could cause the robot to do these things.  
</p>
<h2>
The Asymmetry of Perspective
</h2>
<p>
In short, the perspective of afreeism is asymmetric.  Looking towards the future, we see that what we do matters.  It benefits us to learn, to analyze, and to act.  This is true even though the world is deterministic.  Looking towards the past, however, we see that what we did was inevitable.  We should feel no guilt, no shame, no regret, and no remorse.  Indeed, there are only two reasons to look at the past at all.  The first is to enjoy the good memories.  The second is to learn from our past mistakes.
</p>
<p>
The Stoics of ancient Greece and Rome, who we will encounter again in later chapters, were determinists who had precisely this perspective, both looking forward to the future and looking backwards to the past.  As William Irving writes:
</p>
<p>
[The Stoics] are advising us to be fatalistic with respect to the past, to keep firmly in mind that the past cannot be changed.  Thus, the Stoics would not counsel a mother with a sick child to be fatalistic with respect to the future; she should try to nurse the child back to health (even though the Fates have already decided whether the child lives or dies).  But if the child dies, they will counsel this woman to be fatalistic with respect to the past.  It is only natural, even for Stoics, to experience grief after the death of a child.  But to dwell on that death is a waste of time and emotions, inasmuch as the past cannot be changed. 
</p>
<p>
It is interesting, but completely consistent with the Stoic acceptance of determinism, that some of the greatest Stoic philosophers were people of action.  Seneca (born probably around 1-4 BCE) was a philosopher, playwright, Roman senator, and advisor to the emperor Nero.  Another Stoic philosopher, Marcus Aurelius, was the emperor of Rome from 61 CE until his death in 80 CE.  Like Stoicism, afreeism counsels action, looking forward.  Looking to the past, it counsels acceptance and tolerance.
</p>
<p>
To summarize, causation has different implications depending on whether one is looking forward or looking backwards.  Looking forward through the lens of causation we see that the future depends on what we do in each instance.  It therefore benefits us to give some attention to the future.  Again, this statement is true even though the world is deterministic.  Looking backwards through that same lens, we realize that, because of the chain of causation, what happened (including all of the decisions we made) had to happen.  
</p>
<p>
This perspective, which happens to represent our best conception of reality, can be very disconcerting at first, but ultimately it feels quite liberating.  The rest of this book is, in a sense, about the implications of understanding the deterministic nature of the universe.
</p>



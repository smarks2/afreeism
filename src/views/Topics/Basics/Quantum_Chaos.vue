    <p>At this point, we must discuss one little wrinkle in the deterministic universe. As we discussed above, random events are generally determined events about which we do not have sufficient information to make predictions. The study of subatomic particles, however, has led to the theory of quantum mechanics, which includes the idea that there may be truly random events, at least at the subatomic level.</p>
    <p>Quantum mechanics can possibly affect the macro universe through chaotic interactions. Chaos theory holds that tiny effects can have large consequences. Here is a well-known description of a chaotic chain:</p>
    <p style="margin-left:20px;">
      For want of a nail, the shoe was lost;
      <br />For want of the shoe, the horse was lost;
      <br />For want of the horse, the rider was lost;
      <br />For want of the rider, the battle was lost;
      <br />For want of the battle, the kingdom was lost . . .
    </p>
    <p>Another popular characterization of chaotic systems is expressed by the term “butterfly effect” after meteorologist Edward Lorenz posed the question, at a conference presentation, “Does the flap of a butterfly’s wing in Brazil set off a tornado in Texas?” The answer is that it could.</p>
    <p>When applied to quantum mechanics this leaves open the possibility that subatomic interactions––which according to quantum mechanics may be truly random––could generate large effects thereby introducing true randomness into the universe. This has led some philosophers to believe that quantum randomness gives them an opening to argue for the existence of free will. Yet such an opening is illusory. Note the following:</p>
    <p
      style="margin-left:20px;"
    >1. It is still unclear whether there are truly random events. In other words, it is possible that a deterministic model could someday displace quantum mechanical randomness. (Albert Einstein was famously skeptical of the randomness in quantum mechanics. “God does not play dice,” he said.) Indeed, such models already exist, such as the De Broglie–Bohm theory, the many-worlds (multiverse) model pioneered by Hugh Everett III , and the more recent quantum causal model of Allen et al. (2017).</p>
    <p
      style="margin-left:20px;"
    >2. The quantum mechanical model is, at a minimum, stochastically deterministic. Even if there is true randomness, the probabilities are determined. That is, if two states can exist probabilistically, the probabilities are fixed.</p>
    <p
      style="margin-left:20px;"
    >3. Even if such genuine randomness exists, it does not imply free will. Indeed, it is antithetical to it.</p>
    <p>The third point is the most telling. How indeed can an action be simultaneously random and the product of deliberative free will? Imagine a neural network in a dog. Suppose one applies a stimulus and the effect is the dog barking. The stimulus stimulates a receptor nerve or nerves (for example, the auditory nerves) and then travels deterministically along a neural path, ultimately stimulating a complex of muscles, leading to a bark. Accordingly, the stimulus leads deterministically to the response. There is no free will. Now, let us interrupt this path with a randomizing neuron. (I am just making this up. There is no evidence that such a neuron can exist.) This neuron, suppose, randomly interrupts the signal. Sometimes it goes through and the dog barks. Sometimes it does not. Is there any sense that the insertion of this random neuron in the causal chain results in free will?</p>


Romantic love is not a matter of choice.  It makes sense in a deterministic world.  Likewise, anger, anxiety, worry, hate, friendship, empathy, and kindness are all at root neurological phenomena that can and do exist in the deterministic world.  For this reason, we may someday be able to create robots that emote.
</p>
<p>
This is not to say that an afreeist understanding has no effect on how we feel.  Indeed, some of our emotions could be profoundly affected.  Once we understand the deterministic nature of the universe, we will be less prone to judging others and more accepting of them.  We will be less prone to anger and will be less likely to feel regret, remorse, or guilt.  I will discuss this more in Part III.  

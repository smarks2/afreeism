<P>
The arguments for afreeism are simple and straightforward.  Events are caused.  
We humans are not exempt from the laws of physics and causation.  Thus events, 
including human choices, are determined.  Nevertheless, one struggles with the 
idea that we have no free will because it challenges our sense of personal autonomy.
</p>
<p>

Personal autonomy is the sense that we are free and independent agents.  We do what we want. We weigh our options and we make decisions.  Afreeism throws doubt on how free we really are.  Are we not just actors reciting our prescribed lines in a play that was written eons ago?  And, if so, what meaning could this life possibly have?  One is reminded of this famous passage of Shakespeare:  
</p>
<p style="margin-left:20px;">
Lifeâ€™s but a walking shadow, a poor player<br />

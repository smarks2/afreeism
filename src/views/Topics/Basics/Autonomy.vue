<h2>Autonomy</h2>

<p>
Notice what we have done with our thought experiment.  We have created a society of autonomous robots.  Once we construct Robot World, we can step back and watch it operate.  The robots take in inputs and make decisions.  They communicate with one another through declarative statements, conditional statements, questions, and commands.  They organize their society and create rules for living.  
</p>
<p>
Still, everything is caused and what happens is therefore completely determined.  If we know all the algorithms and the beginning set of inputs, then we should be able to predict every single action and every single utterance of our robots.  They have no free will.  Of course, if we populate our Robot World with too many robots the level of complexity may make prediction difficult.  Nevertheless, Robot World is completely deterministic.
</p>
<p>
Our robots are autonomous because they have the ability to deliberate, and to act according to this deliberation.  They have the ability to take action, to help determine the outcome of history and the course of their world.  
</p>
<p>
All of this is to say that autonomy exists in the deterministic universe.  Autonomy without free will.

<p>
This is completely contrary to how we like to see ourselves.  We like to think of ourselves as making our own decisions.  We are not merely actors reciting prewritten lines.  No one tells us what to do.  
</p>
<p>
Philosophers who oppose the idea of determinism like to trot out stories that illustrate how horrible a deterministic world would be.  The philosopher Daniel Dennett has catalogued a number of these stories.  They include those of the Invisible Jailor, the Nefarious Neurosurgeon, the Hideous Hypnotist, the Peremptory Puppeteer, the Cosmic Child Whose Dolls We Are, the Malevolent Mindreader, and the Digger Wasp. 
</p>
<p>
For example, if determinism is true, are we not, in effect, like prisoners in an invisible jail, under the control of an invisible jailor?  We think we have freedom, but we really do not.  The jailor is secretly constraining us.  Alternatively, perhaps we are in the clutches of a nefarious neurosurgeon who has secretly implanted electrodes in our brains and by means of electrical impulses controls our wants and desires.  We act according to those wants and desires, and thus we think we are free.  However, to control our actions all the nefarious neurosurgeon has to do is to stimulate our brains to change our wants and desires.  Or maybe we have been hypnotized and our actions are controlled by a hypnotist.  Again, we believe we are free, but in reality, the hypnotist controls us.  (The Peremptory Puppeteer, the Cosmic Child Whose Dolls We Are and the Malevolent Mindreader are similar stories.)
</p>
<p>
The Digger Wasp story is slightly different.  The digger wasp is an actual insect with known behaviors.  Scientists have learned to manipulate some of these behaviors.  If we put the wasp in a particular situation, it will act in a particular way.  If we do it over and over again, the wasp will perform identically.  The behavior is completely mechanical, predictable, and determined.  Perhaps we humans are like that.
</p>
<p>
These are all stories about how horrible the world would be if determinism were true.  Of course, just because something is horrible does not mean it is not true.  Thus, these arguments are not really arguments against determinism or against the lack of free will, even though they are presented as such.  The overwhelming evidence is that the universe is deterministic and that we have no free will.  If there are unfortunate consequences, then so be it.
</p>
<p>
However, it turns out that such a world is not so horrible after all.  As Dennett points out, none of these stories are good analogies for life in a deterministic world.  Although our actions are determined, they are not determined by another human being who secretly controls us.  There is no Invisible Jailor, Nefarious Neurosurgeon, Hideous Hypnotist, or the like.  Second, and this is directed towards the Digger Wasp analogy, although our actions are determined, the causal mechanism is quite different than that of other organisms.  Unlike the digger wasp, we use reason.  We look at the consequences of our actions.  We make predictions about the future.  We are not acting instinctually even though our choices are causally determined.
</p>
<p>
Nevertheless, our lives are still determined by causal factors that were unleashed long before we were born.  Completely determined.  All that we think, say, do, and feel, has been determined. This is the truth of the world.  We need to get comfortable with that fact and what it implies.  Fortunately, the implications are not as bad as some would have us believe.  In fact, understanding determinism and its implications for free will can make our lives and the world better.  So rather than putting our heads in the sand and pretending that determinism is not true, let us try to understand it.
</p>


    <div class="TopicHeading">Moral Responsibility</div>
      <p>
Moral responsibility is perhaps the raison d’être of the free will debate.  (This will become very apparent in Chapters 5 and 6.)  This is not to say that it is the only issue.  However, without the issue of moral responsibility, it is likely that no one would care much about free will.  The argument goes like this:  If people do not have free will, how can we hold them morally responsible for actions that they have taken?  And without moral responsibility, what becomes of human behavior?  Why would anyone act morally if we do not hold them accountable?
</p>
<p>
Moral responsibility is tied to the idea of desert.  The noun desert (stress on the second syllable) refers to the concept of deserving.  It also signifies that which is deserved.  If one receives her just deserts, she is getting what she deserves.   To say that a person is morally responsible for an action is to say that they deserve punishment or reward for having taken that action.
</p>
<h2>
The Nature of Moral Responsibility
</h2>
<p>
Free will is a physical phenomenon, which is to say that whether it exists or not depends on how the physical laws of the universe work.  Free will can exist only if people have the physical ability to do otherwise from what they actually do.  Since they do not, free will cannot exist.
</p>
<p>
Moral responsibility is different.  Moral responsibility is a social and psychological construct.  This is to say that people are morally responsible for their past actions only if we (society) say and feel that they are.  The question is whether it makes sense to say that people are moral responsible for actions that they have taken. 
</p>
<h2>
Desert
</h2>
<p>
Because both desert and moral responsibility mean various things in the English language, I want to be clear what we mean by each.  Let us begin with desert.
</p>
<p>
Sometimes we reward a person after she has done some good behavior or punish her for after she has done some bad behavior.  We justify these rewards and punishment in various different ways.  Consider two different justifications:
</p>
<p style="margin-left:20px;">
Desert:  We punish Person X because she deserves to be punished for committing a bad act.  We reward Person X, because she deserves the reward for committing some good act.
</p>
<p style="margin-left:20px;">
Incentives:  We punish Person X in order to incentivize (deter) her and persons like her from committing the bad act in the future.  We reward Person X to incentivize (encourage) her and persons like her to do similar acts in the future.
</p>
<p>
Thus desert means something distinct from creating incentives.  The following statements are expressions of desert:
</p>
<p style="margin-left:20px;">
The driver of a getaway car in a bank robbery deserves a five-year prison sentence.
</p>
<p style="margin-left:20px;">
The salesperson who worked hard and produced a good sales record deserves a promotion.
</p>
<p>
The word deserve has other uses in the English language as well, and this sometimes leads to confusion.  For example, consider these statements:
</p>
<p style="margin-left:20px;">
The sick child deserves care and medical attention.
</p>
<p style="margin-left:20px;">
The beautiful building deserves to be preserved.
</p>
<p style="margin-left:20px;">
The poor guy deserves a bit of good luck.
</p>
<p style="margin-left:20px;">
The American public deserve good government.
</p>
<p>
These uses of the word deserve are different than the two previously mentioned ones because they do not involve punishments or rewards, at least not explicitly.  I will not be using deserve in these ways.  For our purposes, deserve will mean the appropriateness of receiving a reward (or punishment) simply because the action is good (or bad) and the actor chose to do it.
</p>
<p>
In contrast, rewards and punishment can instead be based on incentives.  Using the incentive justification, we punish or reward people to affect their future actions and the future actions of others.
</p>
<p>
Sometimes we justify a consequence based on both desert and incentives.  For example, we may say that the punishment for murder may be deserved and may also serve to deter.  Other consequences may be justified on deterrence alone.  For example, some cities have enacted congestion fees for driving in the city during certain hours.  The car is fitted with a transponder and if the car enters the controlled zone, the driver’s credit card is charged.  This is designed to deter driving in order to reduce congestion.  Nevertheless, we do not pass moral judgment on someone who drives in the city during the specified hours.  She is simply choosing to do so and accepts the price.  Likewise, we may justify some consequences on desert alone.  Some would say, for example, that a thief deserves to be punished even if the punishment had absolutely no deterrent effect.
</p>
<p>
Note that consequences may have other rationales as well.  The government may impose monetary consequences for actions simply to raise revenue.  For example, a tax for fishing (through the sale of fishing licenses) may serve simply to raise revenue to pay for fish management.  In addition, we may isolate a dangerously violent person merely to remove them from society, or quarantine the carrier of an infectious disease for the same reason.
</p>
<p>
One way to determine whether a punishment or reward is based on desert is to ask the following question:  if we eliminate all other rationales, do we still feel that the person should be rewarded or punished?  For example, if were convinced that the consequence would not deter, would not raise revenue, would not protect the public through isolation, etc., do we still believe that the person should be punished?  If we answer yes, we are probably basing our consequence on desert.
</p>
<h2>
Moral Responsibility
</h2>
<p>
Moral responsibility is just another way of expressing the idea of desert.  Consider the following statement:
</p>
<p style="margin-left:20px;">
The driver who drove the getaway car in the bank robbery is morally responsible for her action.
</p>
<p>
This statement is just saying that the driver deserves punishment.  
</p>
<p>
Along the same lines, philosopher Derk Pereboom defines moral responsibility as follows:
</p>
<p style="margin-left:20px;">
[F]or an agent to be morally responsible for an action is for it to belong to her in such a way that she would deserve blame if she understood that it was morally wrong, and she would deserve credit or perhaps praise if she understood that it was morally exemplary. 
</p>
<p>
When philosophers talk about moral responsibility, it is this sort of definition that they typically have in mind.  According to the definition, someone is morally responsible for an action if she deserves blame or credit for the action.  
</p>
<p>
That said, moral responsibility is sometimes used in a different sense, which is simply to acknowledge the existence of a moral rule.  Consider this statement:
</p>
<p style="margin-left:20px;">
Parents have a moral responsibility to provide their children with a good education.
</p>
<p>
When moral responsibility is used this way, it really means moral obligation.  So, we could say:
</p>
<p style="margin-left:20px;">
Parents have a moral obligation to provide their children with a good education.
</p>
<p>
Or, 
</p>
<p style="margin-left:20px;">
Parents should provide their children with a good education.
</p>
<p>
This simply expresses a moral rule.  It says nothing about the consequences of failing to abide by it.  Some philosophers will claim that a rule is not a rule without an enforcement mechanism.  John Stuart Mill was of this mind.  He writes: 
</p>
<p>
We do not call anything wrong unless we mean to imply that a person ought to be punished in some way for doing it - if not by law, by the opinion of his fellow creatures; if not by opinion, by the reproaches of his own conscience.
</p>
<p>

</p>
<p>
However, there is no reason why a rule requires punishment to enforce it.  For example, we could program our robots in Robot World to follow certain moral rules.  They would do so, even without punishment.
</p>
<h2>
The Psychology of Desert
</h2>
<p>
Above I stated that people are morally responsible for their past actions only if we (society) say and feel that they are.  Feeling is an important aspect of desert.  When we believe that people deserve something, it just feels good when they get it.  The psychology surrounding this is interesting.  As neurobiologist Robert Sapolsky notes, punishing someone for violating moral norms . . .
</p>
<p style="margin-left:20px;">
. . . is a deep, atavistic pleasure.  Put people in brain scanners, give them scenarios of norm violations.  Decision making about culpability for the violation correlates with activity in the cognitive dlPFC.  But decision making about appropriate punishment activates the emotional vmPFC, along with the amygdala and insula; the more activation, the more punishment.  The decision to punish, the passionate motivation to do so, is a frothy limbic state. . . . Punishment just feels that good. 
</p>
<p>
However, punishment only feels good if we believe that the person who receives the punishment deserves it.  Punishing the wrong person, or punishing someone who could not have done otherwise, brings no pleasure at all.  We do not get pleasure from seeing someone punished who was caused to do the act because of an epileptic seizure or a brain tumor.
</p>
<h2>
The Argument
</h2>
<p>
Although desert and moral responsibility are social and psychological phenomena, they depend on the physical phenomenon of free will, that is, on the ability to do otherwise.  Without free will, or the ability to do otherwise, the concepts of desert and moral responsibility have no application.  In what follows, I am going to argue that no person is ever morally responsible for the actions she has taken.  That is, a person never deserves reward or punishment.
</p>
<p>
More precisely, the reasoning is this:
</p>
<p style="margin-left:20px;">
Premise 1:  A person deserves the consequences of her actions only if she could have done otherwise.  If she could not have done otherwise, then she cannot deserve what happens as a result.
</p>
<p style="margin-left:20px;">
Premise 2:  Since the universe is deterministic, no person could have done other than what she did.  
</p>
<p style="margin-left:20px;">
Conclusion:  Thus a person never deserves the consequences of her actions; no person ever deserves punishment or reward.  That is, no person is ever morally responsible for an action that she has taken.
</p>
<p>
In what follows, I will give a more detailed explanation of the above, and will explore some of the consequences of these findings.
</p>
<h2>
The Ability To Do Otherwise
</h2>
<p>
For someone to deserve a punishment or reward, she has to have the ability to make a free choice.  Sometimes we express this as follows: 
</p>
<p>
A person deserves a consequence (a desert), only if,
</p>
<p style="margin-left:20px;">
1.	The person committed the act.
</p>
<p style="margin-left:20px;">
2.	The act had some moral character.  That is, the act was either good or bad.  
</p>
<p style="margin-left:20px;">
3.	The person knew or should have known the moral character of the act.  That is, if the act was morally wrong, the person knew or should have known that the act was morally wrong. If it were exemplary, then the person knew that.
</p>
<p style="margin-left:20px;">
4.	The person could have done otherwise.
</p>
<p>
Without these conditions, there is no desert.  Indeed, this is another way to identify whether we are basing a consequence on desert or on some other justification.  We ask the following question:  if we were to learn that the person had to do what they did, that they could not do otherwise, would we still want to punish or reward her?
</p>
<p>
I should note that these are minimal conditions.  There are situations that satisfy these conditions and yet there is still no desert.  For example, a driver who is speeding because a carjacker is holding a gun to her head satisfies these conditions, since technically the driver could have refused to speed and risked getting shot.  We would not say that a driver who speeds in these circumstances deserves punishment.
</p>
<p>
The ability to do otherwise is key to desert.  If a person did not have the ability to do otherwise, then she cannot deserve punishment or reward.  A few thought experiments will confirm this.  Consider three scenarios:
</p>
<p style="margin-left:20px;">
1.	A person pulls the trigger and kills someone out of a desire to collect the victim’s life insurance. 
</p>
<p style="margin-left:20px;">
2.	A person who is examining a gun pulls the trigger and kills someone as the result of seizure caused by a heretofore-unknown brain tumor.
</p>
<p style="margin-left:20px;">
3.	During brain surgery to remove a benign tumor, an evil neurosurgeon secretly implants an electrode in the brain of a subject.  When triggered, the electrode creates an irresistible desire in the subject to kill the first person she sees.  The evil neurosurgeon triggers the electrode and the subject kills the person in the room with her at the time.
</p>
<p>
In the second two cases, most people have no problem concluding that the subject does not deserve punishment.  The reason is that the subject could have done otherwise, but chose not to.  Even in the third case, where the subject did what she wanted to do, her desire was manipulated so that her choice was inevitable.
</p>
<p>
What many people do not realize, however, is that the first case is similar to the other two.  Even in the first case, a chain of causation puts everything into motion resulting in events unfolding in one particular way. A chain of causation led to the killing in the first case just as surely as it did in the second and third cases.  Thus, in the deterministic universe (the one in which we live), there is no justification for desert, as we have defined it in any of the three cases.  The fourth condition is never satisfied.
</p>
<p>
I should mention that some philosophers have argued that desert exists even in a deterministic universe.  To do this, they have had to abandon condition 4 above, that one has to have the ability to do otherwise.  They would like to say that a person deserves punishment in the first case but not in the other two.  But since they concede that the actor had no ability to do otherwise in any of the three cases, how do they distinguish the first case from the other two?    No one has ever succeeded in doing so, but the attempts are very interesting.  We examine such arguments in Chapter 6.
</p>
<h2>
Robot World Revisited
</h2>
<p>
We might ask this question:  even if desert for past actions does not make much sense, should we nevertheless embrace it for pragmatic reasons?  For example, we could program our robots as follows:
</p>
<p style="margin-left:20px;">
1.  We would have our robots adopt the usual notion of desert.  Thus they would punish and reward only if there were an ability to do otherwise.  
</p>
<p style="margin-left:20px;">
2.  We would deceive our robots into believing that for some actions there was an ability to do otherwise.  That is, we would deceive them into believing that free will exists.  (We would also have to give our robots some algorithm for labeling some actions as the product of free will and other as not.)
</p>
<p>
There are several with this second step.  Since we know that there is never an ability to do otherwise, it is not clear how we can program our robots to identify actions where they would apply desert.  We would need trick our robots into dividing actions into those where there is an ability to otherwise and others where there is no such ability.  To so, we would need some other criteria that would do the dividing while making our robots believe that it is on the basis of the ability to do otherwise.  
</p>
<p>
The question is whether it is useful to go through this charade?  There are a number of problems with this approach.  First, if we make our robots rational enough, they will soon discover that their world is deterministic and therefore there is never an ability to do otherwise.  That is, they will see through the charade.  (As we are seeing through it!)  Thus they would never apply rewards or punishment based on desert.
</p>
<p>
Second, it is unnecessary.  Indeed, if the reason we are giving our robots such feelings is to modify future behavior, then what we are really interested in is incentives.  Incentives is a tool that gets at exactly what we want to get at:  behavior.  Why not simply program our robots to recognize good and bad behavior and the sense to apply rewards and punishments to encourage or deter?
</p>
<p>
Finally, it is counterproductive.  Even if we trick our robots into believing that there sometimes is the ability to do otherwise, we may find our robots punishing and rewarding where the punishment or reward does little or no good.  Or we they may punish too much or too little.  Indeed, in human world, many people suffer guilt, shame, regret or remorse over actions that they have taken.  Also,  people apply or support the application of inhumane and ineffective punishments on the basis of desert (retribution).  In both cases, the amount of suffering may substantially outweigh whatever social benefit such feelings could have in modifying future behavior.
</p>
<h2>
Implications of Afreeism
</h2>
<p>
Afreeism and its rejection of desert and moral responsibility for past actions embraces the following ideas:
</p>
<p style="margin-left:20px;">
1.	There can be (and, in fact, there are) moral rules.  (This was discussed in Chapters 2 and 3.)
</p>
<p style="margin-left:20px;">
2.	Nevertheless, one never deserves blame, punishment, praise or reward for any action or inaction that one has taken.  Because the universe is deterministic, because a person could not have done otherwise, that person cannot deserve blame, punishment, praise, or reward, even if she fails to abide by a moral rule.
</p>
<p style="margin-left:20px;">
3.	There may be other ways to justify punishment and rewards.
</p>
<p style="margin-left:20px;">
4.	A system of punishment and rewards will likely look quite different once we abandon desert as a justification.  I will discuss this in future chapters.  In brief, the severity of punishments will likely go down.  Instead, we will punish those who commit crimes more like we punish our children, that is, in order to teach them and deter them, but we will do so with restraint and a bit of sadness.  We will also recalibrate rewards.  Extremes is wealth and poverty may no longer be justified.
</p>
<p style="margin-left:20px;">
5.	Finally, what applies to others applies to ourselves.  We do not deserve punishment or rewards either.  We again explore this is later chapters.  We will see that feelings of guilt, shame, regret, and remorse are no longer justified.  This removes impediments to joy.
</p>
<p>
Of course, there are still unresolved issues.  For example, punishing a person always involves punishing someone who, in an important sense, is innocent, given that in a deterministic universe, everyone is always innocent.  We therefore need a theory of punishment that accepts this fact.  We look at this in Chapter 10.
</p>
<p>
To summarize, afreeism holds that desert is not justified and that we are not morally responsible for past actions.  This will significantly affect the way we approach the world.  We will see that the rejection of the idea of desert has major implications for social policy, including how we approach the criminal law system, freedom and regulation, income and wealth distribution, foreign policy, immigration, and affirmative action, among others.  It can also affect us more personally, influencing how we treat others and ourselves.   This change in perspective is further discussed in the following chapter.
</p>
